<h2>Planting </h2>

 <ul class="text">
  <li> Plant seed potatoes (pieces of whole potato or a small whole potato, with at least 2 eyes per piece) 0-2 weeks after last spring frost.  </li>
  <li>If you are cutting up potato pieces for planting, do so a 1-2 days ahead of time. This will give them the chance to form a protective layer, both for moisture retention and rot resistance.</li>
  <li> You may start planting earlier, as soon as soil can be worked, but be aware that some crops will be ruined by a frost.</li>
  <li>Spread and mix in rotted manure or organic compost in the bottom of the trench before planting.</li>
    
    <li>Plant seed potatoes one foot apart in a 4-inch deep trench, eye side up.
Practice yearly crop rotation.</li>
<li>
Before planning your garden, take a look at our plant companions chart to see which veggies are compatible.</li>

   </ul>
<hr>


   <h2>CARE</h2>
<h3>HOW TO GROW POTATOES</h3>
<ul class="text">
<li>
Potatoes thrive in well-drained, loose soil.</li>
<li>
Potatoes need consistent moisture, so water regularly when tubers start to form.</li>
<li>
Hilling should be done before the potato plants bloom, when the plant is about 6 inches tall. Hoe the dirt up around the base of the plant in order to cover the root as well as to support the plant. Bury them in loose soil. The idea is to keep the potato from getting sunburned, in which case they turn green and will taste bitter.</li>
<li>
You will need to hill potatoes every couple of weeks to protect your crop.</li>

</ul>
<hr>

<h2>PESTS/DISEASES </h2>
<ul class="text">
<li>
Aphids</li>
<li>
Flea Beetles</li>
<li>

Leaf Hoppers</li>
<li>
Early/Late Blight</li>
<li>
Potato Scab: Most likely cause by soil with high pH. Remember: Potatoes like acidic soil (do not plant in soil with a pH higher than 5.2). Dust seed potatoes with sulfur before planting.</li>
</ul>
<hr/>


<h2>HARVEST/STORAGE</h2>

<h3>HOW TO HARVEST POTATOES</h3>
<ul class="text">
<li>
Dig potatoes on a dry day. Dig up gently, being careful not to puncture the tubers. The soil should not be compact, so digging should be easy.</li>
<li>
New potatoes will be ready for harvest after 10 weeks, usually in early July.</li>
<li>
You should harvest all of your potatoes once the vines die (usually by late August), or the potatoes may rot.</li>
<li>
Make sure you brush off any soil clinging to the potatoes, then store them in a cool, dry, dark place. The ideal temperature for storage is 35 to 40°F.</li>
<li>
Do not store potatoes with apples; their ethylene gas will cause potatoes to spoil.
Whether you dig your own potatoes or buy them at a store, don’t wash them until right before you use them. Washing potatoes shortens their storage life.</li>
<li>
Find more tips on getting potatoes ready for the root cellar.</li>
</ul>
<hr/>


 
<h2>RECOMMENDED VARIETIES</h2>
<ul class="text">
<li>
‘Irish Cobbler’ is an early variety.</li>
<li>
‘Viking’ is a red skinned potato, regular season variety.</li>
<li>
‘Chieftan’ is resistant to potato scab.</li>
<li>
‘Elba’, ‘Rosa’ and ‘Sebago’ are all somewhat resistant to blight.
Try our page on how to choose the best potatoes to grow in your garden.</li>
</ul>
